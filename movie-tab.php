    <div class="media-body">
     <p><br><b>The Dark Knight</b> is a 2008 superhero film directed, produced, and co-written by Christopher Nolan. Based on the DC Comics character Batman, the film is the second part of Nolan's Batman film series and a sequel to 2005's Batman Begins. Christian Bale reprises the lead role of Bruce Wayne/Batman, with a returning cast of Michael Caine as Alfred Pennyworth, Gary Oldman as James Gordon and Morgan Freeman as Lucius Fox. The film introduces the character of Harvey Dent (Aaron Eckhart), Gotham's newly elected District Attorney and the consort of Bruce Wayne's childhood friend Rachel Dawes (Maggie Gyllenhaal), who joins Batman and the police in combating the new rising threat of a criminal mastermind calling himself "The Joker" (Heath Ledger).Nolan's inspiration for the film was the Joker's comic book debut in 1940, the 1988 graphic novel The Killing Joke, and the 1996 series The Long Halloween, which retold Two-Face's origin. The nickname "the Dark Knight" was first applied to Batman in Batman No. 1 (1940), in a story written by Bill Finger. The Dark Knight was filmed primarily in Chicago, as well as in several other locations in the United States, the United Kingdom, and Hong Kong. Nolan used an IMAX camera to film some sequences, including the Joker's first appearance in the film. On January 22, 2008, some months after he had completed filming on The Dark Knight and six months before the film's release, Heath Ledger died from a toxic combination of prescription drugs, leading to intense attention from the press and movie-going public. Warner Bros. had initially created a viral marketing campaign for The Dark Knight, developing promotional websites and trailers highlighting screen shots of Ledger as the Joker, but after Ledger's death, the studio refocused its promotional campaign.
